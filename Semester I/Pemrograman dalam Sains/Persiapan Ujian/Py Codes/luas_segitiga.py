# program untuk menghitung luas segitiga
a = input("Masukkan alas segitiga: ")
t = input("Masukkan tinggi segitiga: ")
a = float(a)
t = float(t)
L = (1/2)*(a*t)
L = round(L,4)
print("Luas segitiganya adalah: ",L,"cm^2")
